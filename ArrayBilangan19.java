public class ArrayBilangan19 {
    public static void main(String[] args){
        int[] bil = new int[4];

        //Mengisi elemen array dengan nilai yang telah ditentukan
        bil[0] = 5;
        bil[1] = 13;
        bil[2] = -7;
        bil[3] = 17;

        //Menampilkan semua array ke layar
        System.out.println(bil[5.0]);
        System.out.println(bil[12867]);
        System.out.println(bil[7.5]);
        System.out.println(bil[2000000]);
    }
}